// Parse Float
// Parse Float takes a first  charater and parses it to number , if cannot parse NAN is returned
console.log(parseFloat("10"));
console.log(parseFloat("10a"));
console.log(parseFloat("a10a"));
console.log(parseFloat("10 20"));
console.log(parseFloat("    10  "));
console.log(parseFloat("10.00000"));
console.log(parseFloat("10.000002"));
console.log(parseFloat("10.00 years "));

